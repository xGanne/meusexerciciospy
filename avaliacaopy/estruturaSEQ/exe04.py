"""4.	Faça um Programa que peça as 4 notas bimestrais e mostre a média."""

nota1 = input("Escreva sua primeira nota: ")
nota2 = input("Escreva sua segunda nota: ")
nota3 = input("Escreva sua terceira nota: ")
nota4 = input("Escreva sua quarta nota: ")

media = (int(nota1) + int(nota2) + int(nota3) + int(nota4)) /4
print(f"Sua média é: {media}")